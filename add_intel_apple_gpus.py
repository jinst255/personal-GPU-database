def make_search_url(site, query):
    """Generate search URL for a given site and GPU name."""
    encoded = query.replace(" ", "%20")
    if site == "newegg":
        return f"https://www.newegg.com/p/pl?d={encoded}"
    elif site == "amazon":
        return f"https://www.amazon.com/s?k={encoded}"
    elif site == "bh":
        return f"https://www.bhphotovideo.com/c/search?q={encoded}"
    elif site == "ebay":
        return f"https://www.ebay.com/sch/i.html?_nkw={encoded}%20GPU&LH_Sold=1"
    return N


def calc_derived(row):
    """Calculate cost_per_gb and performance-per-dollar fields."""
    r = dict(row)

    def safe_float(val):
        if val == N or val is None:
            return None
        try:
            return float(val)
        except (ValueError, TypeError):
            return None

    def safe_int(val):
        if val == N or val is None:
            return None
        try:
            return int(float(val))
        except (ValueError, TypeError):
            return None

    price_new = safe_float(r.get("price_usd_new_current"))
    price_used = safe_float(r.get("price_usd_ebay_used"))
    vram = safe_float(r.get("vram_gb"))
    fp32 = safe_float(r.get("fp32_tflops"))

    # cost_per_gb_vram_new_usd
    if price_new and vram and vram > 0:
        r["cost_per_gb_vram_new_usd"] = round(price_new / vram, 2)
    else:
        r["cost_per_gb_vram_new_usd"] = N

    # cost_per_gb_vram_used_usd
    if price_used and vram and vram > 0:
        r["cost_per_gb_vram_used_usd"] = round(price_used / vram, 2)
    else:
        r["cost_per_gb_vram_used_usd"] = N

    # fp32_per_dollar_new
    if fp32 and price_new and price_new > 0:
        r["fp32_per_dollar_new"] = round(fp32 / price_new, 4)
    else:
        r["fp32_per_dollar_new"] = N

    # fp32_per_dollar_used
    if fp32 and price_used and price_used > 0:
        r["fp32_per_dollar_used"] = round(fp32 / price_used, 4)
    else:
        r["fp32_per_dollar_used"] = N

    return r


