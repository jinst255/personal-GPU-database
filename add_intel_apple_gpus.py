def build_row(gpu_data):
    """Build a complete row dict with all 58 columns, filling missing with null."""
    row = {}
    for col in COLUMNS:
        if col == "last_modified":
            row[col] = TIMESTAMP
        elif col in ("cost_per_gb_vram_new_usd", "cost_per_gb_vram_used_usd",
                      "fp32_per_dollar_new", "fp32_per_dollar_used"):
            row[col] = N  # will be calculated
        else:
            row[col] = gpu_data.get(col, N)
    # Calculate derived fields
    row = calc_derived(row)
    return row


def rows_match(r1, r2):
    """Check if two rows have the same gpu_name."""
    return r1.get("gpu_name") == r2.get("gpu_name")


def main():
    # Read existing CSV
    if not os.path.exists(CSV_PATH):
        print(f"ERROR: CSV file not found at {CSV_PATH}")
        sys.exit(1)

    with open(CSV_PATH, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        existing_rows = list(reader)
        fieldnames = reader.fieldnames

    print(f"Existing rows: {len(existing_rows)}")
    existing_names = {r["gpu_name"] for r in existing_rows}

    # Combine all new GPU data
    all_new_data = (
        intel_consumer_gpus
        + intel_professional_gpus
        + intel_datacenter_gpus
        + apple_gpus
    )

    # Build complete rows and filter out duplicates
    new_rows = []
    for gpu in all_new_data:
        row = build_row(gpu)
        if row["gpu_name"] in existing_names:
            print(f"  SKIP (already exists): {row['gpu_name']}")
            continue
        new_rows.append(row)

    if not new_rows:
        print("No new rows to add.")
        return

    # Append new rows in batches of 15
    BATCH_SIZE = 15
    total_added = 0

    combined_rows = existing_rows + new_rows

    with open(CSV_PATH, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, quoting=csv.QUOTE_MINIMAL)
        writer.writeheader()
        for i, row in enumerate(combined_rows):
            writer.writerow(row)
            if i >= len(existing_rows):
                total_added += 1

    print(f"\nTotal new rows added: {total_added}")
    print(f"Total rows in CSV: {len(combined_rows)}")

    # Summary breakdown
    intel_consumer_added = sum(
        1 for r in new_rows
        if r.get("manufacturer") == "Intel" and r.get("category") == "consumer"
    )
    intel_pro_added = sum(
        1 for r in new_rows
        if r.get("manufacturer") == "Intel" and r.get("category") == "professional"
    )
    intel_dc_added = sum(
        1 for r in new_rows
        if r.get("manufacturer") == "Intel" and r.get("category") == "datacenter"
    )
    apple_added = sum(
        1 for r in new_rows if r.get("manufacturer") == "Apple"
    )

    print(f"\nBreakdown:")
    print(f"  Intel Arc Consumer:       {intel_consumer_added}")
    print(f"  Intel Arc Professional:   {intel_pro_added}")
    print(f"  Intel Datacenter:         {intel_dc_added}")
    print(f"  Apple M-Series:           {apple_added}")

    # Print GPU names added
    print(f"\nGPUs added:")
    for r in new_rows:
        derived_str = ""
        pn = r.get("price_usd_new_current")
        fp32 = r.get("fp32_tflops")
        if pn != N and fp32 != N:
            derived_str = f" [${pn}, {fp32} TFLOPS]"
        print(f"  - {r['gpu_name']}{derived_str}")


if __name__ == "__main__":
    main()
